# example 2

my_tuple = ('t', 'u', 'r', 'i', 'a', 'l', 's', 'p','o', 'i', 'n', 't')
print(my_tuple[1:])   # Print elements from index 1 to end 
print(my_tuple[:2])   # from start to index 2 
print(my_tuple[5:12]) # from index 1 to index 3 
print(my_tuple[::5])  # from start to end using step size